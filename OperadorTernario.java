public class OperadorTernario {
  public static void main(String[] args) {
    // variavel
    double nota = 8;

    //operador ternario
    System.out.println( nota >= 7 ? "Aprovado(a)" : "Reprovado(a)");
    
    String situacao = nota  <= 5 ? "Aprovado(a)" : "Reprovado(a)";

    System.out.println(situacao);
  
  }
}
