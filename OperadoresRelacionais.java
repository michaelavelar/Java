public class OperadoresRelacionais {
  public static void main(String[] args) {
    // variveis
    int numero1 = 10;
    int numero2 = 5;

    // Operadores Relacionais
    System.out.println(numero1 > numero2); //maior
    System.out.println(numero1 >= numero2); // maior ou igual
    System.out.println(numero1 < numero2); // menor
    System.out.println(numero1 <= numero2); // menor ou igual
    System.out.println( numero1 == numero2); //comparando valor igual
    System.out.println(numero1 != numero2 ); //comparando valor diferente

  }
}
